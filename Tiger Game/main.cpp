//put all jump-able locations around source to av[].
void avaliable(int src, int length)
{
    setArrayValue(av,-1,V);
    int j = 0;
    dijkstra(src);
    for (int i = 0; i < V; i++)
    {
        if (dist[i] <= length && i!=src)
        {
            av[j] = i;
            j++;
        }
    }
}

// return the distance between source and destination.
int routeLength(int src,int dest)
{
    if(src<=112&&src>=0&&dest<=112&&dest>=0)
    {
    dijkstra(src);
    return dist[dest];
    }
    else return 999999;
}

// put the path from source to destination to path[].
void routePath(int src,int dest)
{
    int i = routeLength(src,dest) - 2;
    setArrayValue(path,-1,V);
    setArrayValue(pre,-1,V);
    dijkstra(src);
       while (dest >= src)
        {
            dest = pre[dest];
            path[i] = dest;
            i--;
        }
}

